/// \file SteppingAction.cc
/// \brief Implementation of the SteppingAction class

#include "SteppingAction.hh"
#include "EventAction.hh"
#include "DetectorConstruction.hh"

#include "G4Step.hh"
#include "G4RunManager.hh"

//....oooOO0OOooo........oooOO0OOooo........oooOO0OOooo........oooOO0OOooo......

SteppingAction::SteppingAction(
                      const DetectorConstruction* detectorConstruction,
                      EventAction* eventAction,
                      std::array<G4double, DetectorConstruction::LayerNumber> *edepArray)
  : G4UserSteppingAction(),
    fDetConstruction(detectorConstruction),
    fEventAction(eventAction),
    fEdep(edepArray)
{}

//....oooOO0OOooo........oooOO0OOooo........oooOO0OOooo........oooOO0OOooo......

SteppingAction::~SteppingAction()
{}

//....oooOO0OOooo........oooOO0OOooo........oooOO0OOooo........oooOO0OOooo......

void SteppingAction::UserSteppingAction(const G4Step* step)
{
// Collect energy and track length step by step

  // get volume of the current step
  auto volume = step->GetPreStepPoint()->GetTouchableHandle()->GetVolume();
  // identify layer
  G4int volID = volume->GetCopyNo();
  // energy deposit
  auto edep = step->GetTotalEnergyDeposit();
  G4cout << "energy deposit at layer no. " << volID << " is " << edep << "\n";

  if (1000 <= volID && volID < 1000 + DetectorConstruction::LayerNumber) {
    fEdep->at(volID-1000) += edep;
  }

}

